import java.util.Scanner;

public class Persegi {
        public static void main(String[] args) {
            Scanner input =new Scanner(System.in);
    
            double panjang;
            double luas;
            double sisi;
            double keliling;
    
            panjang = input.nextInt();
    
            luas = panjang * luas;
            keliling = sisi * 4;
    
            System.out.println("Luas: " +luas);
            System.out.println("Keliling: " +keliling);
    
        }
    } 

